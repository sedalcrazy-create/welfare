<script setup>
import { useRoute } from 'vue-router'
import { computed } from 'vue'

const route = useRoute()

const isActive = (name) => {
  return computed(() => {
    if (name === 'home') {
      return route.name === 'home'
    }
    if (name === 'guests') {
      return route.name === 'guests'
    }
    if (name === 'letters') {
      return route.name === 'letters' || route.name === 'letter-detail'
    }
    if (name === 'quotas') {
      return route.name === 'quotas'
    }
    return false
  })
}
